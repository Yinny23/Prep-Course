Conceptos

//Bucles for: Permiten evaluar expresiones una y otra vez hasta que devuelve lo que estamos buscando o se detiene después de cierto tiempo.
//Estos bucles tienen una sintaxis única, similar a la instrucción if, pero un poco más compleja. Necesitaremos tres cosas: 
//Primero debemos declarar una variable, esto es sobre lo que se repetirá el bucle.
//Segundo a través de una expresión condicional, el ciclo continuará sucediendo hasta que esta declaración sea "false"
//Tercero, incrementamos nuestra variable.

//&&: Es un operador lógico también conocido como "Y" ("AND"). Permite evaluar dos expresiones y devuelve verdadero si ambas expresiones son "true".
//Si uno (o ambos) de ellos es falso, este operador devolverá "false".

//||: Es un operador lógico también conocido como "Ó" ("OR"). Determina si una de las dos expresiones es "true" y devuelve "true" si una o (ambas) 
//de las expresiones es "true". O devuelve "false" si ambas expresiones son "false".

//!: Es un operador lógico también conocido como "NOT" ("NO"). Permite negar una expresión o igualdad, devuelven el valor booleano opuesto 
//de lo que se le pasa. 